## The Engineering Problem
The Cauvery River would frequently cause devastating floods during the monsoon, drowning crops and villages. However, during the blistering summer, the water would run dry. Karikala Chola needed a massive structure to slow down the floodwaters, prevent the water from uselessly draining into the sea, and permanently divert it into the fertile delta region for year-round farming.

## Unbelievable Construction Techniques
Building a dam across a powerful, constantly flowing river with a soft, sandy bed (without modern cement or underwater concrete) seemed impossible. The ancient engineers used a revolutionary technique:

1. **The Interlocking Stone Method**: They brought massive, unhewn boulders of granite to the river.
2. **Sinking the Foundation**: They dropped the giant rocks into the riverbed. The sheer force of the flowing water eroded the sand beneath the rocks, causing them to sink deep into the riverbed naturally until they hit bedrock.
3. **The Clay Cement**: Once the foundation was stable, they stacked thousands of stones on top. To bind them, they used an incredibly strong, waterproof, traditional mortar made of sticky clay, lime, and crushed organic matter.
