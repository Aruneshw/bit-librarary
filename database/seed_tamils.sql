Long before the invention of formal scripts like Tamil-Brahmi or Vatteluttu, ancient humans needed a way to communicate, identify property, and record information. They achieved this by carving **incised marks and symbols** (often called pottery graffiti) into the surface of clay pots. These symbols act as a fascinating window into the minds and society of prehistoric and ancient Tamil people.

